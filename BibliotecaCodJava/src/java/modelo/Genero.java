package modelo;

public class Genero {

    // Atributos
    private int idGenero;
    private String nombre;

    // Getters y Setters
    public int getIdGenero() { 
        return idGenero; 
    }

    public void setIdGenero(int idGenero) { 
        this.idGenero = idGenero; 
    }

    public String getNombre() { 
        return nombre; 
    }

    public void setNombre(String nombre) { 
        this.nombre = nombre; 
    }
}
